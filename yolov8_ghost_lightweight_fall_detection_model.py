"""---
## Step 3 — Define GhostNeck-YOLOv8n Architecture

### Key design decision
The Backbone is kept identical to the original YOLOv8n so that COCO pretrained weights
can be loaded without modification. Only the Neck (FPN + PAN path) is replaced with
Ghost-based C3Ghost blocks, which halve Neck parameters via cheap depthwise feature generation.
"""

# Write the GhostNeck-YOLOv8n architecture YAML.
# nc is set to 80 here (COCO default) — Ultralytics overrides it at training time
# using the nc value in data_colab.yaml.
ghost_yaml_content = """
# GhostNeck-YOLOv8n
# Backbone: unchanged YOLOv8n (loads COCO pretrained weights)
# Neck: C2f replaced by C3Ghost; strided Conv replaced by GhostConv
# Reference: Han et al., GhostNet, CVPR 2020

nc: 80

scales:
  n: [0.33, 0.25, 1024]

# ----- Backbone (unchanged from YOLOv8n) -----
backbone:
  - [-1, 1, Conv, [64, 3, 2]]         # 0  P1/2
  - [-1, 1, Conv, [128, 3, 2]]        # 1  P2/4
  - [-1, 3, C2f, [128, True]]
  - [-1, 1, Conv, [256, 3, 2]]        # 3  P3/8
  - [-1, 6, C2f, [256, True]]
  - [-1, 1, Conv, [512, 3, 2]]        # 5  P4/16
  - [-1, 6, C2f, [512, True]]
  - [-1, 1, Conv, [1024, 3, 2]]       # 7  P5/32
  - [-1, 3, C2f, [1024, True]]
  - [-1, 1, SPPF, [1024, 5]]          # 9

# ----- Neck + Head (Ghost modules) -----
head:
  - [-1, 1, nn.Upsample, [None, 2, 'nearest']]  # 10  upsample P5 -> P4 scale
  - [[-1, 6], 1, Concat, [1]]                    # 11  concat with backbone P4
  - [-1, 3, C3Ghost, [512]]                       # 12  Ghost bottleneck (replaces C2f)

  - [-1, 1, nn.Upsample, [None, 2, 'nearest']]  # 13  upsample -> P3 scale
  - [[-1, 4], 1, Concat, [1]]                    # 14  concat with backbone P3
  - [-1, 3, C3Ghost, [256]]                       # 15  Ghost bottleneck (P3 output)

  - [-1, 1, GhostConv, [256, 3, 2]]              # 16  Ghost strided conv (downsample)
  - [[-1, 12], 1, Concat, [1]]                   # 17  concat with neck P4
  - [-1, 3, C3Ghost, [512]]                       # 18  Ghost bottleneck (P4 output)

  - [-1, 1, GhostConv, [512, 3, 2]]              # 19  Ghost strided conv (downsample)
  - [[-1, 9], 1, Concat, [1]]                    # 20  concat with backbone SPPF
  - [-1, 3, C3Ghost, [1024]]                      # 21  Ghost bottleneck (P5 output)

  - [[15, 18, 21], 1, Detect, [nc]]              # 22  detection head (P3, P4, P5)
"""

GHOST_YAML = "/content/yolov8n-ghostneck.yaml"
with open(GHOST_YAML, 'w') as f:
    f.write(ghost_yaml_content)

print(f"GhostNeck-YOLOv8n architecture YAML saved: {GHOST_YAML}")
print("Design: Backbone unchanged | Neck uses C3Ghost + GhostConv")

