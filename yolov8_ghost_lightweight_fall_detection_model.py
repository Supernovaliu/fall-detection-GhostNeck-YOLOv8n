    name    = "fall_detection_ghostneck",

    # Optimiser — identical to baseline for a fair comparison
    optimizer      = "SGD",
    lr0            = 0.01,
    lrf            = 0.01,
    momentum       = 0.937,
    weight_decay   = 0.0005,
    warmup_epochs  = 3.0,
    warmup_momentum= 0.8,

    # Augmentation — identical to baseline
    mosaic       = 1.0,
    mixup        = 0.1,
    copy_paste   = 0.0,
    close_mosaic = 10,
    fliplr       = 0.5,
    flipud       = 0.0,
    degrees      = 5.0,
    translate    = 0.1,
    scale        = 0.5,
    shear        = 2.0,
    perspective  = 0.0,
    hsv_h        = 0.015,
    hsv_s        = 0.7,
    hsv_v        = 0.4,
    erasing      = 0.0,

    patience = 20,
    seed     = 42,    # same seed as baseline for fair comparison
    workers  = 2,
    exist_ok = True,
    verbose  = True,
