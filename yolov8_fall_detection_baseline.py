def draw_yolo_bbox(img, label_path, class_names=["fall_detected", "sitting", "walking"],
                   colors=[(0, 0, 255), (0, 200, 0), (255, 165, 0)]):
    """Draw YOLO-format bounding boxes on an image."""
    h, w = img.shape[:2]
    if not os.path.exists(label_path):
        return img
    with open(label_path) as f:
        for line in f:
            parts = line.strip().split()
            if len(parts) < 5:
                continue
            cls = int(parts[0])
            xc, yc, bw, bh = [float(x) for x in parts[1:5]]
            x1 = int((xc - bw / 2) * w)
            y1 = int((yc - bh / 2) * h)
            x2 = int((xc + bw / 2) * w)
            y2 = int((yc + bh / 2) * h)
            color = colors[cls] if cls < len(colors) else (255, 255, 0)
            label = class_names[cls] if cls < len(class_names) else f"cls_{cls}"
            cv2.rectangle(img, (x1, y1), (x2, y2), color, 2)
            cv2.putText(img, label, (x1, y1 - 8),
                        cv2.FONT_HERSHEY_SIMPLEX, 0.7, color, 2)
    return img

