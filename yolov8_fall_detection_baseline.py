"""---
## Step 5 — Train YOLOv8n Baseline

### Augmentation strategy

| Parameter     | Value | Rationale                                              |
|---------------|-------|--------------------------------------------------------|
| mosaic        | 1.0   | 4-image mosaic — increases scene diversity             |
| mixup         | 0.1   | Blends two images — improves generalisation            |
| fliplr        | 0.5   | Horizontal flip — falls are direction-agnostic         |
| flipud        | 0.0   | No vertical flip — indoor cameras don't invert         |
| degrees       | 5.0   | Slight rotation — simulates tilted cameras             |
| hsv_v         | 0.4   | Brightness variation — simulates day/night lighting    |
| perspective   | 0.0   | No perspective warp — preserves human body proportions |
| close_mosaic  | 10    | Disable mosaic in final 10 epochs for stable convergence|
"""

from ultralytics import YOLO

# ============================================================
# TRAINING CONFIG — adjust for quick tests (e.g. epochs=10)
# ============================================================
MODEL_NAME = "yolov8n.pt"   # COCO pretrained weights (nano variant)
EPOCHS     = 100
BATCH_SIZE = 16             # safe for T4 (14 GB VRAM)
IMG_SIZE   = 640
OUTPUT_DIR = "/content/runs"
# ============================================================

# Load YOLOv8n with COCO pretrained weights (transfer learning)
model = YOLO(MODEL_NAME)

# Start training
results = model.train(
    data    = FINAL_YAML,
    epochs  = EPOCHS,
    imgsz   = IMG_SIZE,
    batch   = BATCH_SIZE,
    project = OUTPUT_DIR,
    name    = "fall_detection",

    # Optimiser
    optimizer      = "SGD",
    lr0            = 0.01,    # initial learning rate
    lrf            = 0.01,    # final lr = lr0 * lrf
    momentum       = 0.937,
    weight_decay   = 0.0005,
    warmup_epochs  = 3.0,     # linear warmup for 3 epochs
    warmup_momentum= 0.8,

    # Mosaic / MixUp augmentation
    mosaic       = 1.0,   # 4-image mosaic probability
    mixup        = 0.1,   # MixUp blend probability
    copy_paste   = 0.0,   # copy-paste disabled
    close_mosaic = 10,    # disable mosaic in last 10 epochs

    # Geometric augmentation
    fliplr      = 0.5,    # horizontal flip probability
    flipud      = 0.0,    # vertical flip disabled (indoor setting)
    degrees     = 5.0,    # rotation +/-5 deg
    translate   = 0.1,    # translation +/-10%
    scale       = 0.5,    # scale +/-50%
    shear       = 2.0,    # shear +/-2 deg
    perspective = 0.0,    # perspective disabled (preserves body proportions)

    # Colour augmentation
    hsv_h = 0.015,  # hue variation
    hsv_s = 0.7,    # saturation variation
    hsv_v = 0.4,    # brightness variation (day/night simulation)

    # Misc
    erasing  = 0.0,   # random erasing disabled
    patience = 20,    # early stopping: halt if no improvement for 20 epochs
    seed     = 42,    # fixed seed for reproducibility
    workers  = 2,     # Colab recommended
    exist_ok = True,
    verbose  = True,
)

